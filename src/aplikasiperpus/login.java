    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = user.getText();
        String password1 = password.getText();
        String cekuser = null;
        String cekpassword = null;
        try {
            Connection connect = koneksi.getKoneksi();
            Statement st = (Statement) connect.createStatement();
            String sql = "SELECT * FROM staff WHERE username = '"+username+"' AND password = '"+password1+"'";
            java.sql.ResultSet res = st.executeQuery(sql);
            while(res.next()){
                cekuser = res.getString("username");
                cekpassword = res.getString("password");
            }
            res.close();
            st.close();
        } catch (SQLException e){
            System.out.print("Terjadi Error");
        }
        if(cekuser == null && cekpassword== null){
            String pesan = "Username Atau Password Salah";
            JOptionPane.showMessageDialog(null,pesan,"Message",
            JOptionPane.INFORMATION_MESSAGE);
        } else {
            String pesan1 = "Login Berhasil";
            JOptionPane.showMessageDialog(null,pesan1,"Message",
            JOptionPane.INFORMATION_MESSAGE);
            new perpustakaan().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

